package management.student.ManagementStudent;

public class Student {

  private String name; //名前
  private int age; //年齢

  //以降setter,getter
  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getAge() {
    return age;
  }

  public void setAge(int age) {
    this.age = age;
  }


}
